#include "libft.h"

int	ft_isalnum(int c)
{
	int	isalnum;

	isalnum = 0;
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122)
		|| (c >= 48 && c <= 57))
		isalnum = 1;
	return (isalnum);
}
